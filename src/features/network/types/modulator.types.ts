export interface ModulatorEffect {
  /**
   * Влияние на порог спайка (мВ или относительное смещение)
   * Положительное значение → повышает порог (труднее спайковать)
   * Отрицательное → снижает порог (легче спайковать)
   * Пример: -5 → при полной концентрации порог снижается на 5 мВ
   */
  thresholdShift?: number;

  /**
   * Влияние на мембранную "утечку" (leak) или постоянную времени (tau)
   * Значение от 0 до 1: 
   *   < 1 → уменьшает утечку → потенциал дольше сохраняется (tau растёт)
   *   > 1 → увеличивает утечку → быстрее возврат к покоя (tau падает)
   * Пример: 0.9 → утечка снижается на 10%
   */
  tauMultiplier?: number;

  /**
   * Влияние на синаптическую проводимость (усиление входного тока)
   * > 1 → усиливает синаптический вход
   * < 1 → ослабляет
   * Пример: 1.25 → вход усиливается на 25%
   */
  conductanceMultiplier?: number;

  /**
   * (Опционально) Собственная кинетика модулятора — для облака
   * Скорость распада концентрации за шаг: new = old * decay
   * Пример: 0.95 → за 20 шагов спадает до ~35%
   */
  decayRate?: number;

  /**
   * (Опционально) Максимальный радиус действия (в условных единицах)
   */
  radius?: number;
}